const result = 90;

// if: membuat satu kondisi
if (result > 90) {
  console.log("Grade: A");
}

// else if: membuat 2 kondisi atau lebih
else if (result > 80) {
  console.log("Grade: B");
} else if (result > 70) {
  console.log("Grade: C");
}

// else: membuat kondisi terakhir
else {
  console.log("Grade: D");
}


